package com.bm.model;

public class SoftwareInputModel {
	private int software_Id;
	private String software_Name;
	private String software_Version;
	private String software_Desc;
	
	public int getSoftware_Id() {
		return software_Id;
	}
	public void setSoftware_Id(int software_Id) {
		this.software_Id = software_Id;
	}
	public String getSoftware_Name() {
		return software_Name;
	}
	public void setSoftware_Name(String software_Name) {
		this.software_Name = software_Name;
	}
	public String getSoftware_Version() {
		return software_Version;
	}
	public void setSoftware_Version(String software_Version) {
		this.software_Version = software_Version;
	}
	public String getSoftware_Desc() {
		return software_Desc;
	}
	public void setSoftware_Desc(String software_Desc) {
		this.software_Desc = software_Desc;
	}
	
}
